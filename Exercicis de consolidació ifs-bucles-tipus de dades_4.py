num1 = float(input('Introduzca un operador: '))
num2 = float(input('Introduzca un operador: '))
sign = str(input('Introduzca una operando: '))

if sign == "+":
    print(num1+num2)

if sign == "-":
    print(num1-num2)

if sign == "*":
    print(num1*num2)

if sign == "/":
    print(num1/num2)

if sign == "//":
    print(num1//num2)

if sign == "%":
    print(num1%num2)